class exam_6
{
    public static void main (int x)
    {
        switch (x)
        {
            case 1:
                System.out.println ("Good Morning");
                break;
                case 2:
                System.out.println ("Good Afternoon");
                break;
                case 3:
                System.out.println ("Good Evening");
                break;
                case 4:
                System.out.println ("Good Night");
                break;
                default:
                System.out.println ("Wrong choice");
        }
    }
}